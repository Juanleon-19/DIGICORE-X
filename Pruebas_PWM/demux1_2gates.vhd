LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;

------------------------------------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------------------------------------

ENTITY demux1_2gates IS
    
   
    PORT   (
				x : IN STD_LOGIC;
				sel : IN STD_LOGIC;
				y1 : OUT STD_LOGIC;
				y2 : OUT STD_LOGIC
		
           );
    
END ENTITY demux1_2gates;

------------------------------------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------------------------------------

ARCHITECTURE gateLevel OF demux1_2gates IS

	

BEGIN

------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------


	y1 <= x AND (NOT sel);
	y2 <= x AND ( sel);



END gateLevel;