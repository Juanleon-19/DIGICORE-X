LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;

------------------------------------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------------------------------------

ENTITY Mux2_1_Nine_Bit IS
    
   
    PORT   (
				x1 : IN STD_LOGIC_VECTOR(8 DOWNTO 0);
				x2 : IN STD_LOGIC_VECTOR(8 DOWNTO 0);
				sel : IN STD_LOGIC;
				y : OUT STD_LOGIC_VECTOR(8 DOWNTO 0)
		
           );
    
END ENTITY Mux2_1_Nine_Bit;

------------------------------------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------------------------------------

ARCHITECTURE functional OF Mux2_1_Nine_Bit IS


BEGIN

------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------

		with sel select
		 y <= x1 when '0',
				x2 when others;



END functional;