LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;

------------------------------------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------------------------------------

ENTITY FullAdder IS
    
    
    PORT   (X : IN  STD_LOGIC;
            Y : IN  STD_LOGIC;
				C_IN : IN  STD_LOGIC;
            S : OUT STD_LOGIC;
				C_OUT: OUT STD_LOGIC
				
           );
    
END ENTITY FullAdder;

------------------------------------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------------------------------------

ARCHITECTURE operationArch OF FullAdder IS


BEGIN

------------------------------------------------------------
-- 
-- 
-- 
------------------------------------------------------------

		 S <= X XOR Y XOR C_IN;
		
		 C_OUT <= (X AND Y)OR(Y AND C_IN)OR(X AND C_IN);


END operationArch;